#include <stdio.h>

int main()
{
    printf("Hello World !\n");

    //Cac kieu du lieu co ban
    char c = 'A'; //kieu du lieu ki tu
    int i = 10; //kieu du lieu so nguyen
    float a = 10.20; //kieu du lieu so thuc
    double b = 10.20; //kieu du lieu double

    printf("Gia tri cua c la : %c\n", c);
    printf("Gia tri cua i la : %d\n", i);
    printf("Gia tri cua a la : %f\n", a);
    printf("Gia tri cua b la : %lf\n", b);
    return 0;
}